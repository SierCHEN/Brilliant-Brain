<p>在学习计算机网络之前，我们先来理解两个概念，网络是什么？互联网又是什么？</p>
</blockquote>
<p><strong>网络（Network）</strong> 是指由一组具有通信能力的设备相互连接而形成的系统。它可以是一个 <strong>局域网（Local Area Network，LAN）</strong>，也可以是 <strong>广域网（Wide Area Network，WAN）</strong>。局域网通常用于一个办公室、大楼或校园内，而广域网则跨越更大的地理范围。而 <strong>互联网（Internet）</strong> 是网络的网络，它是由多个不同类型的网络通过协议相互连接而形成的庞大网络，覆盖全球范围。</p>
<p>从功能上看，<strong>网络</strong> 提供了传输信息的基础设施，但没有具体的功能。它允许设备之间进行通信和数据共享。<strong>互联网</strong> 则包括了更多的功能和服务，如搜索引擎获取信息、浏览网页、观看视频、购物等。</p>
<p>从组成上来看，<strong>网络</strong> 可以是自治的，即每台主机都有自己的硬件和软件，可以单独运行使用。<strong>互联网</strong> 则是由多个自治的网络通过协议相互连接而形成的，这些协议包括TCP/IP等。</p>
